package com.eastsoft.esgjyj.enums;

/**
 * 枚举用户状态。
 * @author Ben
 * @since 1.0.0
 * @version 1.0.0
 */
public enum UserState {
	ENABLE("0", "启用"),
	DISABLE("1", "停用"),
	PAUSE("2", "暂停");
	
	private final String value;
	
	private final String text;
	
	private UserState(String value, String text) {
		this.value = value;
		this.text = text;
	}

	public String getValue() {
		return value;
	}

	public String getText() {
		return text;
	}
}
